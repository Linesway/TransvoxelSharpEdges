/**
 * One-off script: run from src/tables/unused/mc-polygon with
 *   node build-mc-polygon-table.mjs
 * Writes ../../mc-polygon-table.js. Tables are now baked into mc-extended-tables.js.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { buildMcPolygonTable } from '../table-generation.js';
import { triTable } from '../../mc-tables.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const table = buildMcPolygonTable(triTable);
const outPath = path.join(__dirname, '..', '..', 'mc-polygon-table.js');
const content = `/**
 * MC extended polygon table (precomputed).
 * Row format: [n_components, nv_0, edge_idx..., nv_1, ...], -1 padded to 17.
 * Generated by build-mc-polygon-table.mjs — do not edit by hand.
 */
export const polygonTable = ${JSON.stringify(table)};
`;
fs.writeFileSync(outPath, content);
console.log('Wrote', outPath);
