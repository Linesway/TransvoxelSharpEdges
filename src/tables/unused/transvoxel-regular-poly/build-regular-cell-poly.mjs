/**
 * One-off script: run from src/tables/unused/transvoxel-regular-poly with
 *   node build-regular-cell-poly.mjs
 * Writes ../../regular-cell-poly-table.js. Tables are now baked into transvoxel-extended-tables.js.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { buildRegularCellPolyTable } from '../table-generation.js';
import { regularCellClass, regularCellData } from '../../transvoxel-tables.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const table = buildRegularCellPolyTable(regularCellClass, regularCellData);
const outPath = path.join(__dirname, '..', '..', 'regular-cell-poly-table.js');
const content = `/**
 * Transvoxel regular-cell polygon table (precomputed).
 * Row format: [n_components, nv_0, idx..., nv_1, ...], -1 padded to 32.
 * Generated by build-regular-cell-poly.mjs — do not edit by hand.
 */
export const regularCellPolyTable = ${JSON.stringify(table)};
`;
fs.writeFileSync(outPath, content);
console.log('Wrote', outPath);
